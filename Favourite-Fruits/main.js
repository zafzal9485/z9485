//We have to make an array in which we store 3 of our favourite fruits and write some independent if-
//statements to test it out.*/
var favourite_fruits = ["Mango", "Strawberry", "Grape"];
if (favourite_fruits.includes("Mango")) {
    console.log("You must Really like Mangoes.");
}
if (favourite_fruits.includes("Strawberry")) {
    console.log("You must Really like Strawberries.");
}
if (favourite_fruits.includes("Grape")) {
    console.log("You must Really like Grapes.");
}
if (favourite_fruits.includes("Orange")) {
    console.log("You don't like Oranges.");
}
if (favourite_fruits.includes("Plum")) {
    console.log("You don't like Plums.");
}
